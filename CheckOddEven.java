public class CheckOddEven {
    public static void main(String[] args) {
        int number = -2;
        System.out.println("The number is: " + number);

        if(number % 2 == 0) {
            System.out.println("EVEN");
        } else {
            System.out.println("ODD");
        }

        System.out.println("bye!");
    }
}
